package math.math;

public class MathMethods {
    public static void main(String[] args) {
        System.out.println(Math.max(13, 7));
        System.out.println(Math.min(13, 7));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.round(2.743));
        System.out.println(Math.abs(-13.5));
        System.out.println(Math.negateExact(33));
        System.out.println(Math.addExact(21, 12));
        System.out.println(Math.subtractExact(21, 12));
        System.out.println(Math.multiplyExact(21, 12));
        System.out.println(Math.pow(2.5, 2));
        System.out.println(Math.incrementExact(21));
        System.out.println(Math.decrementExact(21));
        System.out.println(Math.random());
    }
}
